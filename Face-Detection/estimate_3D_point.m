function result = estimate_3D_point(c1, c2, u1, v1, u2, v2)

AL1 = [c1(1,2)-c1(3,2)*u1, c1(1,3)-c1(3,3)*u1; c1(2,2)-c1(3,2)*v1, c1(2,3)-c1(3,3)*v1];
x = 4;
BL1 = [c1(3,1)*x*u1+u1-c1(1,1)*x-c1(1,4);c1(3,1)*x*v1+v1-c1(2,1)*x-c1(2,4)];

WL1 = AL1\BL1;
L1P1 = [x WL1'];

x = 25;
BL1 = [c1(3,1)*x*u1+u1-c1(1,1)*x-c1(1,4);c1(3,1)*x*v1+v1-c1(2,1)*x-c1(2,4)];

WL1 = AL1\BL1;
L1P2 = [x WL1'];


%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

AL2 = [c2(1,2)-c2(3,2)*u2, c2(1,3)-c2(3,3)*u2; c2(2,2)-c2(3,2)*v2, c2(2,3)-c2(3,3)*v2];
x = 2;
BL2 = [c2(3,1)*x*u2+u2-c2(1,1)*x-c2(1,4);c2(3,1)*x*v2+v2-c2(2,1)*x-c2(2,4)];

WL2 = AL2\BL2;
L2P1 = [x WL2'];

x = 20;
BL2 = [c2(3,1)*x*u2+u2-c2(1,1)*x-c2(1,4);c2(3,1)*x*v2+v2-c2(2,1)*x-c2(2,4)];

WL2 = AL2\BL2;
L2P2 = [x WL2'];

%%%%%%%%%%%%%%%%%%%
P1 = L1P1';
U1 = [L1P2(1)- L1P1(1);L1P2(2)- L1P1(2);L1P2(3)- L1P1(3)];

P2 = L2P1';
U2 = [L2P2(1)- L2P1(1);L2P2(2)- L2P1(2);L2P2(3)- L2P1(3)];

A = [U1'*U1, -U2'*U1; U1'*U2, -U2'*U2];
B = [P2'*U1 - P1'*U1; P2'*U2-P1'*U2];

W = A\B;

result = ((P1+W(1)*U1)+(P2+W(2)*U2))/2;

end

